<?php
// mendefinisikan variabel
$hitung = 7;
$usia = 60;
$salam = 'Sel';

// mengurangi 2 dan menugaskan  ulang nilai baru kepada variabel terkait
// jadi $hitung = $hitung - 2
$hitung -= 2;
echo "$hitung \n";

// membagi dengan 5 dan menugaskan ulang nilai baru kepada variabel terkait
// jadi $usia = $usia / 5
$usia /= 5;
echo "$usia \n";

// menambahkan string baru dan menugaskan ulang nilai baru kepada variabel terkait
// jadi $salam = $salam . 'Sel!'
$salam .= 'amat!';
echo $salam;